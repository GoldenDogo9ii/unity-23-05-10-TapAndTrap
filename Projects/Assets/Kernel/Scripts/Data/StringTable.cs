        string outStr = "";
        string str1 = "";
        int j = 0, k = 0;
        for (int i = 0; i < chr1.Length; i++)
        {
            tt2[i] = System.BitConverter.GetBytes(chr1[i]);
            if (chr1[i].ToString() == "-")
            {

                tt2[k][j] = (byte)int.Parse(str1);
                tt2[k][j] = (byte)(tt2[k][j] ^ 123444555);
                str1 = "";
                j++;
                continue;
            }
            if (chr1[i].ToString() == "+")
            {
                j = 0;
                k++;
                continue;
            }
            str1 += chr1[i].ToString();
        }
        for (int i = 0; i < k; i++)
        {
            chr1[i] = System.BitConverter.ToChar(tt2[i], 0);
            outStr += chr1[i].ToString();
        }
        return outStr;
